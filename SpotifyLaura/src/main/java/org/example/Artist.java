package org.example;

public class Artist {

    String id;
    String name;
    int popularity;
    int followers;
    String genres;

    public Artist(String id, String name, int popularity, int followers, String genres){
        this.id = id;
        this.name = name;
        this.popularity = popularity;
        this.followers = followers;
        this.genres = genres;
    }

}
