package org.example;

public class Album {

    String title;
    String author;
    String ReleaseDate;
    int totalTracks;


    public Album(String title, String author, String ReleaseDate, int totalTracks){
        this.title = title;
        this.author = author;
        this.ReleaseDate = ReleaseDate;
        this.totalTracks = totalTracks;
    }

}
